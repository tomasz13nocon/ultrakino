package pl.ultrakino.exceptions;

public class TvseriesonlineException extends Exception {

	public TvseriesonlineException() {
		super();
	}

	public TvseriesonlineException(String s) {
		super(s);
	}

	public TvseriesonlineException(String s, Throwable throwable) {
		super(s, throwable);
	}

	public TvseriesonlineException(Throwable throwable) {
		super(throwable);
	}

}
